#include "3-calc.h"

/**
* op_add - Adds two integers
* @a: First int
* @b: Second int
* Return: Sum of a and b
*/

int op_add(int a, int b)
{
return ((a) + (b));
}

/**
* op_sub - Subtracts two integers
* @a: First int
* @b: Second int
* Return: Returns subtracted numbers
*/

int op_sub(int a, int b)
{
return ((a) - (b));
}

/**
* op_mul - Multiplies two integers
* @a: First int
* @b: Second int
* Return: Multiplied numbers
*/

int op_mul(int a, int b)
{
return ((a) * (b));
}

/**
* op_div - Divides two integers
* @a: First int
* @b: Second int
* Return: Divided numbers
*/

int op_div(int a, int b)
{
return ((a) / (b));
}

/**
* op_mod - Mods two integers
* @a: First int
* @b: Second int
* Return: Returns modded ints
*/

int op_mod(int a, int b)
{
return ((a) % (b));
}
